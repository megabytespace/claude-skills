#!/usr/bin/env node
/**
 * For every blog image deleted in the last commit, find the kept image with
 * the same md5 hash and emit a 301 redirect map. Writes:
 *   - src/data/image-redirects.ts (compiled into worker bundle)
 *   - public/_image-redirects.json (debug)
 *
 * Run after any image dedupe pass. Idempotent.
 */
import { createHash } from 'node:crypto';
import { execSync } from 'node:child_process';
import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const IMAGE_DIR = 'public/images/blog';
const ABS_IMAGE_DIR = path.join(ROOT, IMAGE_DIR);

const md5 = (buf) => createHash('md5').update(buf).digest('hex');

// Walk every commit on this branch, find deletions, capture each file's
// blob hash at the commit BEFORE the deletion. Works even when files were
// created and deleted on the same branch.
function deletedWithBlobs() {
  const out = execSync(
    `git log --reverse --pretty=format:'commit %H' --name-status -- '${IMAGE_DIR}/*'`,
    { cwd: ROOT, encoding: 'utf8' },
  );
  let prev = null;
  const map = new Map(); // path → { commit-before-deletion }
  for (const line of out.split('\n')) {
    if (line.startsWith('commit ')) {
      prev = line.slice(7).trim();
      continue;
    }
    const parts = line.split('\t');
    if (parts[0] === 'D' && parts[1] && prev) {
      map.set(parts[1], `${prev}~1`);
    }
  }
  return map;
}

function blobAt(commit, filePath) {
  try {
    return execSync(`git show ${commit}:${filePath}`, {
      cwd: ROOT,
      maxBuffer: 50 * 1024 * 1024,
      stdio: ['ignore', 'pipe', 'ignore'],
    });
  } catch {
    return null;
  }
}

// Public-facing URL: strip the `public/` prefix.
const URL_PREFIX = '/' + IMAGE_DIR.replace(/^public\//, '');

function hashKeptImages() {
  const map = new Map();
  for (const f of readdirSync(ABS_IMAGE_DIR)) {
    if (!/\.(jpg|jpeg|png|webp)$/i.test(f)) continue;
    const full = path.join(ABS_IMAGE_DIR, f);
    const h = md5(readFileSync(full));
    if (!map.has(h)) map.set(h, `${URL_PREFIX}/${f}`);
  }
  return map;
}

function main() {
  const deleted = deletedWithBlobs();
  console.log(`Deleted blog images on this branch: ${deleted.size}`);

  const keptByHash = hashKeptImages();
  console.log(`Kept images on disk: ${keptByHash.size} unique hashes.`);

  const redirects = {};
  let unmapped = 0;
  for (const [rel, commit] of deleted) {
    const blob = blobAt(commit, rel);
    if (!blob) continue;
    const h = md5(blob);
    const target = keptByHash.get(h);
    if (!target) {
      unmapped++;
      continue;
    }
    redirects[`/${rel.replace(/^public\//, '')}`] = target;
  }

  console.log(`Mapped: ${Object.keys(redirects).length}`);
  console.log(`Unmapped (no twin found): ${unmapped}`);

  const tsFile = path.join(ROOT, 'src/data/image-redirects.ts');
  const ts =
    `// Auto-generated by scripts/build-image-redirects.mjs. Do not edit.\n` +
    `// Maps deleted (deduped) blog image paths → canonical kept image path.\n` +
    `export const imageRedirects: Record<string, string> = ${JSON.stringify(redirects, null, 2)};\n`;
  writeFileSync(tsFile, ts, 'utf8');

  const jsonFile = path.join(ROOT, 'public/_image-redirects.json');
  writeFileSync(jsonFile, JSON.stringify(redirects, null, 2), 'utf8');

  console.log(`Wrote ${tsFile}`);
  console.log(`Wrote ${jsonFile}`);
}

main();
